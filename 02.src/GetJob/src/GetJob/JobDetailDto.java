package GetJob;

public class JobDetailDto {

    // 招聘公司名称
    private String companyname;
    
    // 招聘公司联系人
    private String companyhr;
    
    // 招聘公司联系电话
    private String companyphoneno;
    
    // 招聘公司地址
    private String companyaddr;
    
    // 岗位名
    private String jobname;
    
    // 招聘数量
    private String hiringnum;
    
    // 性别
    private String sex;
    
    // 年龄
    private String age;
    
    // 薪资
    private String salary;

    /**
     * @return the companyname
     */
    public String getCompanyname() {
        return companyname;
    }

    /**
     * @return the companyhr
     */
    public String getCompanyhr() {
        return companyhr;
    }

    /**
     * @return the companyphoneno
     */
    public String getCompanyphoneno() {
        return companyphoneno;
    }

    /**
     * @return the companyaddr
     */
    public String getCompanyaddr() {
        return companyaddr;
    }

    /**
     * @return the jobname
     */
    public String getJobname() {
        return jobname;
    }

    /**
     * @return the hiringnum
     */
    public String getHiringnum() {
        return hiringnum;
    }

    /**
     * @return the sex
     */
    public String getSex() {
        return sex;
    }

    /**
     * @return the age
     */
    public String getAge() {
        return age;
    }

    /**
     * @return the salary
     */
    public String getSalary() {
        return salary;
    }

    /**
     * @param companyname the companyname to set
     */
    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    /**
     * @param companyhr the companyhr to set
     */
    public void setCompanyhr(String companyhr) {
        this.companyhr = companyhr;
    }

    /**
     * @param companyphoneno the companyphoneno to set
     */
    public void setCompanyphoneno(String companyphoneno) {
        this.companyphoneno = companyphoneno;
    }

    /**
     * @param companyaddr the companyaddr to set
     */
    public void setCompanyaddr(String companyaddr) {
        this.companyaddr = companyaddr;
    }

    /**
     * @param jobname the jobname to set
     */
    public void setJobname(String jobname) {
        this.jobname = jobname;
    }

    /**
     * @param hiringnum the hiringnum to set
     */
    public void setHiringnum(String hiringnum) {
        this.hiringnum = hiringnum;
    }

    /**
     * @param sex the sex to set
     */
    public void setSex(String sex) {
        this.sex = sex;
    }

    /**
     * @param age the age to set
     */
    public void setAge(String age) {
        this.age = age;
    }

    /**
     * @param salary the salary to set
     */
    public void setSalary(String salary) {
        this.salary = salary;
    }
}
